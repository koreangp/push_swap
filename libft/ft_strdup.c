#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*new;

	new = (char *)malloc(ft_strlen(s) + 1);
	i = 0;
	if (new == NULL)
		return (NULL);
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
